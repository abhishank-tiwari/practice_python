#Creating tuple of single element
print("We are creating tuple as t1 = (1) ")
t1 = (1)
print("On checking class of t1 by type(t1) we will get integer")
print(type(t1))
print("Correct method is t2 = (1,) so putting , is necessary after first element")
t2 = (1,)
print("On checking class of t2 we get tuple ")
print(type(t2))